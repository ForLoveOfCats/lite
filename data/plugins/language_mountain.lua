local syntax = require "core.syntax"


syntax.add {
  files = { "%.mtn$" },
  comment = "//",
  patterns = {
    { pattern = "//[^/].-\n",                type = "comment"    },
    { pattern = "//[^/]",                    type = "comment"    },
    { pattern = "///.-\n",                   type = "doccomment" },
    { pattern = "#[%w_]*",                   type = "string"     },
    { pattern = "\\.-\n",                    type = "string"     },
    { pattern = { '"', '"', '\\' },          type = "string"     },
    { pattern = { "'", "'", '\\' },          type = "string"     },
    { pattern = "0x[%da-fA-F]+",             type = "number"     },
    { pattern = "-?%d+[%d%.eE]*",            type = "number"     },
    { pattern = "-?%.?%d+",                  type = "number"     },
    { pattern = "[%+%-=/%*%^%%<>!~|&:,%.;]", type = "operator"   },
    { pattern = "[%[%]%(%){}]",              type = "bracket"    },
    { pattern = "[%a_][%w_]*%f[(]",          type = "function"   },
    { pattern = "@[%a_][%w_]*",              type = "keyword2"   },
    { pattern = "[%a_][%w_]*",               type = "symbol"     },
  },
  symbols = {
    ["if"]             = "keyword",
    ["else"]           = "keyword",
    ["elif"]           = "keyword",
    ["while"]          = "keyword",
    ["for"]            = "keyword",
    ["break"]          = "keyword",
    ["continue"]       = "keyword",
    ["return"]         = "keyword",
    ["switch"]         = "keyword",
    ["let"]            = "keyword",
    ["alias"]          = "keyword",
    ["as"]             = "keyword",
    ["const"]          = "keyword",
    ["init"]           = "keyword",
    ["make"]           = "keyword",
    ["allocate"]       = "keyword",
    ["throw"]          = "keyword",
    ["func"]           = "keyword",
    ["undefined"]      = "literal",
    ["true"]           = "literal",
    ["false"]          = "literal",
    ["null"]           = "literal",
    ["defer"]          = "keyword",
    ["errdefer"]       = "keyword",
    ["error"]          = "keyword2",
    ["using"]          = "keyword2",
    ["unreachable"]    = "keyword2",
    ["and"]            = "keyword",
    ["or"]             = "keyword",
    ["module"]         = "keyword",
    ["enum"]           = "keyword2",
    ["struct"]         = "keyword2",
    ["union"]          = "keyword2",
    ["implements"]     = "keyword",
    ["test"]           = "keyword",
  },
}
