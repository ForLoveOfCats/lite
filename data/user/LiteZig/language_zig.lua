local syntax = require "core.syntax"


syntax.add {
  files = { "%.zig$" },
  comment = "//",
  patterns = {
    --The commented out patterns rely on style additions yet to be upstreamed
    --https://github.com/rxi/lite/pull/10
    { pattern = "//[^/].-\n",               type = "comment"    },
    { pattern = "//[^/]",                   type = "comment"    },
    --{ pattern = "///.-\n",                  type = "doccomment" },
    { pattern = "\\.-\n",                   type = "string"     },
    { pattern = { '"', '"', '\\' },         type = "string"     },
    { pattern = { "'", "'", '\\' },         type = "string"     },
    { pattern = "0x[%da-fA-F]+",            type = "number"     },
    { pattern = "-?%d+[%d%.eE]*",           type = "number"     },
    { pattern = "-?%.?%d+",                 type = "number"     },
    { pattern = "[%+%-=/%*%^%%<>!~|&:,%.;]", type = "operator"   },
    --{ pattern = "[%[%]%(%){}]",             type = "bracket"    },
    { pattern = "[%a_][%w_]*%f[(]",         type = "function"   },
    { pattern = "@[%a_][%w_]*",             type = "keyword2"   },
    { pattern = "[%a_][%w_]*",              type = "symbol"     },
  },
  symbols = {
    ["if"]             = "keyword",
    ["else"]           = "keyword",
    ["orelse"]         = "keyword",
    ["while"]          = "keyword",
    ["for"]            = "keyword",
    ["break"]          = "keyword",
    ["continue"]       = "keyword",
    ["return"]         = "keyword",
    ["switch"]         = "keyword",
    ["const"]          = "keyword",
    ["try"]            = "keyword",
    ["catch"]          = "keyword",
    ["var"]            = "keyword",
    ["fn"]             = "keyword",
    ["pub"]            = "keyword",
    ["undefined"]      = "literal",
    ["true"]           = "literal",
    ["false"]          = "literal",
    ["null"]           = "literal",
    ["defer"]          = "keyword",
    ["errdefer"]       = "keyword",
    ["error"]          = "keyword2",
    ["usingnamespace"] = "keyword",
    ["unreachable"]    = "keyword2",
    ["align"]          = "keyword",
    ["allowzero"]      = "keyword",
    ["and"]            = "keyword",
    ["or"]             = "keyword",
    ["asm"]            = "keyword",
    ["async"]          = "keyword",
    ["await"]          = "keyword",
    ["enum"]           = "keyword2",
    ["struct"]         = "keyword2",
    ["union"]          = "keyword2",
    ["comptime"]       = "keyword2",
    ["export"]         = "keyword",
    ["extern"]         = "keyword",
    ["inline"]         = "keyword2",
    ["noalias"]        = "keyword",
    ["packed"]         = "keyword",
    ["promise"]        = "keyword",
    ["resume"]         = "keyword2",
    ["suspend"]        = "keyword2",
    ["linksection"]    = "keyword",
    ["test"]           = "keyword",
    ["threadlocal"]    = "keyword",
    ["volatile"]       = "keyword",
  },
}
